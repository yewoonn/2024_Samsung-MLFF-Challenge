class EdgeVariantGF(torch_nn.Module):
    def __init__(self, in_features, out_features, n_shift_taps, n_filter_nodes, bias=True, GSO=None):
        # 초기화
        super(EdgeVariantGF, self).__init__()
        self.in_features = in_features # 32 차원
        self.out_features = out_features # 32 차원
        self.n_shift_taps = n_shift_taps # 3
        self.n_filter_nodes = n_filter_nodes # 32
        self.bias = bias
        self.GSO = GSO 

        # 필터 가중치와 바이어스 학습
		# 필터 가중치: 출력 피처 x 입력 피처 x 필터 탭을 기반으로 한 무작위 값
        # [32, 32, 3]
        self.filter_weights = torch_nn.Parameter(
            torch.randn(out_features, in_features, n_shift_taps)
        )  
        # 바이어스
        if self.bias:
            self.bias_param = torch_nn.Parameter(torch.randn(out_features)) # [32]
		


    def addGSO(self, GSO):
        # GSO 설정 -> 각 노드 간의 연결 관계 반영
        self.GSO = GSO

    def forward(self, x):
        # 입력 데이터 필터링 및 결과 반환"
        # x (torch.Tensor): 입력 노드 피처 (배치 크기 x 입력 피처 수 x 노드 수)
        if self.GSO is None:
            raise ValueError("GSO (Graph Shift Operator)가 설정되지 않았습니다.")

        # 엣지에 대해 필터링을 적용하는 과정
        # oif,bij->bo : 출력 피처와 입력 피처와 필터 탭을 기반으로 필터링 수행
        # filter_weights : 필터 가중치 (out_features x in_features x n_shift_taps)
        # print("self.filter_weights", self.filter_weights.shape)
        # print("x", x.shape)
        filtered_output = torch.einsum('oif,bi->bo', self.filter_weights, x)

        # print("n_shift_taps", self.n_shift_taps)
        
        # 바이어스 적용 (선택)
        if self.bias:
            filtered_output += self.bias_param
        
        
        return filtered_output # torch.Tensor: 필터링된 출력


    
class RealAgnosticResidualInteractionBlock(InteractionBlock):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        # EVGF 초기화
        self.edge_variant_gf = EdgeVariantGF(
            in_features=self.node_feats_irreps.dim, 
            out_features= self.node_feats_irreps.dim, #3
            n_shift_taps= 4,  # 예시로 3개의 shift taps 사용
            n_filter_nodes=self.node_feats_irreps.dim,  # 예시로 노드 수 사용
            bias=True
        )

        # 선형 변환 레이어 정의 (512 -> 32)
        # self.linear_transform = torch.nn.Linear(512, 32)


    def _setup(self) -> None:
        # First linear
        self.linear_up = o3.Linear(
            self.node_feats_irreps,
            self.node_feats_irreps,
            internal_weights=True,
            shared_weights=True,
        )
        # TensorProduct
        irreps_mid, instructions = tp_out_irreps_with_instructions(
            self.node_feats_irreps,
            self.edge_attrs_irreps,
            self.target_irreps,
        )

        self.conv_tp = o3.TensorProduct(
            self.node_feats_irreps, # x 
            self.edge_attrs_irreps, # y 
            irreps_mid,
            instructions=instructions,
            shared_weights=False,
            internal_weights=False,
        )

        # Convolution weights
        input_dim = self.edge_feats_irreps.num_irreps
        self.conv_tp_weights = nn.FullyConnectedNet(
            [input_dim] + 3 * [64] + [self.conv_tp.weight_numel],
            torch.nn.functional.silu,
        )

        # Linear
        irreps_mid = irreps_mid.simplify()
        self.irreps_out = self.target_irreps
        self.linear = o3.Linear(
            irreps_mid, self.irreps_out, internal_weights=True, shared_weights=True
        )

        # Selector TensorProduct
        self.skip_tp = o3.FullyConnectedTensorProduct(
            self.node_feats_irreps, self.node_attrs_irreps, self.hidden_irreps
        )
        self.reshape = reshape_irreps(self.irreps_out)

    def addGSO(self, GSO):
        # EVGF에 GSO 추가
        self.edge_variant_gf.addGSO(GSO)

    def forward(
        self,
        node_attrs: torch.Tensor,
        node_feats: torch.Tensor,
        edge_attrs: torch.Tensor,
        edge_feats: torch.Tensor,
        edge_index: torch.Tensor,
    ) -> Tuple[torch.Tensor, torch.Tensor]:

        sender = edge_index[0] # [15360] 
        receiver = edge_index[1] # [15360]
        num_nodes = node_feats.shape[0] # 1536
        sc = self.skip_tp(node_feats, node_attrs)
        node_feats = self.linear_up(node_feats) # [1536, 32]
        tp_weights = self.conv_tp_weights(edge_feats) # [15360, 128]
    
        # Edge-Variant Graph Filter (EVGF) 적용
        node_feats_filtered = self.edge_variant_gf(node_feats) # [1536, 32] -> EVGF에 out_features 32로 바꿔서 해결

        # print(f"sender : {sender.shape} receiver : {receiver.shape}\n")
        # print(f"num_nodes : {num_nodes} node_feats : {node_feats.shape} tp_weights : {tp_weights.shape}\n")
        # print(f"node_feats_filtered[sender] : {node_feats_filtered[sender].shape} edge_attrs : {edge_attrs.shape} node_feats_irreps.dim : {self.node_feats_irreps.dim}\n")

        # input : [15360, 32] / [15360, 16] / [15360, 128]
        mji = self.conv_tp(
            node_feats_filtered[sender], edge_attrs, tp_weights
        )  # [n_edges, irreps] 
        message = scatter_sum(
            src=mji, index=receiver, dim=0, dim_size=num_nodes
        )  # [n_nodes, irreps]
        message = self.linear(message) / self.avg_num_neighbors

        # print("self.reshape(message)", self.reshape(message).shape) # 4 : [1536, 32, 16]
        # print("sc", sc.shape) # [1536, 32]

        return (
            self.reshape(message),
            sc,
        ) # [n_nodes, channels, (lmax + 1)**2]
    


@compile_mode("script")
class ScaleShiftBlock(torch.nn.Module):
    def __init__(self, scale: float, shift: float):
        super().__init__()
        self.register_buffer(
            "scale", torch.tensor(scale, dtype=torch.get_default_dtype())
        )
        self.register_buffer(
            "shift", torch.tensor(shift, dtype=torch.get_default_dtype())
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.scale * x + self.shift

    def __repr__(self):
        return (
            f"{self.__class__.__name__}(scale={self.scale:.6f}, shift={self.shift:.6f})"
        )
